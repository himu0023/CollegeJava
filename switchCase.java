public class switchCase {
    public static void main(String[] args) {
        int movie = 4;
        switch (movie) {
            case 1:
                System.out.println("Jack Reacher");
                break;

            case 2:
                System.out.println("Mission Impossible");
                break;

            case 3: 
                System.out.println("Transformers");
                break;

            case 4:
                System.out.println("Breaking Bad");
                break;
            
            case 5: 
                System.out.println("Game of Thrones");

            default:
                break;
        }
    }
}
